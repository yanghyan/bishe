	//购物车减一
	function doMinus(id){
		$.ajax({
			type:'POST',
			url:'/bishe/CLY/index.php/Home/Detail/minusCart',
			//url:'/bishe/CLY/index.php/Home/Cart/minusCart',
			data:'mid='+id,
			dataType:'json',
			success:function(res){
				if(res.num>0){
					$("#menu"+res.mid).html(res.num);
					var sum = parseInt($("#sum").html())-parseInt(res.price);
					$("#sum").html(sum);
					if(sum<20){
							var cha = 20-sum;
							$(".pay").html("<span class='cart-desc cart-cha fr'  id='cartCha'>还差&#165;"+cha+"元起送</span>");
						}
				}else{
					$("#cartid"+res.mid).remove();
					var sum = parseInt($("#sum").html())-parseInt(res.price);
					if(sum>0){
						$("#sum").html(sum);	
					}else{
						$("#cartIsEmpty").html("购物车为空");
						$(".pay").hide();
					}
				}
			}
		});
	}

